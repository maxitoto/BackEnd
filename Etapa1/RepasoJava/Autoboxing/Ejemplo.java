package RepasoJava.Autoboxing;

//autoboxing -> primitivo a objeto de forma automatica
//unboxing ->objeto a primitivo de forma automatica

public class Ejemplo {
    Integer enteroObj = 10; //el valor 10 es un int primitivo pero se castea de forma automatica (Autoboxing) en un Integer(Objeto)

    int enteroPrimitivo = enteroObj;// el valor del atributo del objeto pasa a un primitivo de forma automatica
}